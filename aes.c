static const uint8_t rcon[11] = {
    0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
};

static uint8_t xtime(uint8_t x)
{
    return (uint8_t)((x << 1) ^ ((x >> 7) * 0x1b));
}

static uint8_t multiply(uint8_t x, uint8_t y)
{
    uint8_t r = 0;
    while (y) {
        if (y & 1) {
            r ^= x;
        }
        x = xtime(x);
        y >>= 1;
    }
    return r;
}

void aes128_init(aes128_ctx *ctx, const uint8_t key[16])
{
    uint8_t temp[4];
    size_t bytes = 16;
    int rcon_iter = 1;

    memcpy(ctx->round_key, key, 16);
    while (bytes < sizeof(ctx->round_key)) {
        temp[0] = ctx->round_key[bytes - 4];
        temp[1] = ctx->round_key[bytes - 3];
        temp[2] = ctx->round_key[bytes - 2];
        temp[3] = ctx->round_key[bytes - 1];

        if ((bytes % 16) == 0) {
            uint8_t t = temp[0];
            temp[0] = (uint8_t)(sbox[temp[1]] ^ rcon[rcon_iter++]);
            temp[1] = sbox[temp[2]];
            temp[2] = sbox[temp[3]];
            temp[3] = sbox[t];
        }

        for (int i = 0; i < 4; i++) {
            ctx->round_key[bytes] = (uint8_t)(ctx->round_key[bytes - 16] ^ temp[i]);
            bytes++;
        }
    }
}

static void add_round_key(uint8_t state[4][4], const uint8_t *round_key)
{
    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            state[r][c] ^= round_key[c * 4 + r];
        }
    }
}

static void sub_bytes(uint8_t state[4][4])
{
    for (int r = 0; r < 4; r++) {
        for (int c = 0; c < 4; c++) {
            state[r][c] = sbox[state[r][c]];
        }
    }
}

static void inv_sub_bytes(uint8_t state[4][4])
{
    for (int r = 0; r < 4; r++) {
        for (int c = 0; c < 4; c++) {
            state[r][c] = rsbox[state[r][c]];
        }
    }
}

static void shift_rows(uint8_t state[4][4])
{
    uint8_t t;

    t = state[1][0];
    state[1][0] = state[1][1];
    state[1][1] = state[1][2];
    state[1][2] = state[1][3];
    state[1][3] = t;

    t = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = t;
    t = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = t;

    t = state[3][3];
    state[3][3] = state[3][2];
    state[3][2] = state[3][1];
    state[3][1] = state[3][0];
    state[3][0] = t;
}

static void inv_shift_rows(uint8_t state[4][4])
{
    uint8_t t;

    t = state[1][3];
    state[1][3] = state[1][2];
    state[1][2] = state[1][1];
    state[1][1] = state[1][0];
    state[1][0] = t;

    t = state[2][0];
    state[2][0] = state[2][2];
    state[2][2] = t;
    t = state[2][1];
    state[2][1] = state[2][3];
    state[2][3] = t;

    t = state[3][0];
    state[3][0] = state[3][1];
    state[3][1] = state[3][2];
    state[3][2] = state[3][3];
    state[3][3] = t;
}

static void mix_columns(uint8_t state[4][4])
{
    for (int c = 0; c < 4; c++) {
        uint8_t a0 = state[0][c];
        uint8_t a1 = state[1][c];
        uint8_t a2 = state[2][c];
        uint8_t a3 = state[3][c];
        state[0][c] = (uint8_t)(multiply(a0, 2) ^ multiply(a1, 3) ^ a2 ^ a3);
        state[1][c] = (uint8_t)(a0 ^ multiply(a1, 2) ^ multiply(a2, 3) ^ a3);
        state[2][c] = (uint8_t)(a0 ^ a1 ^ multiply(a2, 2) ^ multiply(a3, 3));
        state[3][c] = (uint8_t)(multiply(a0, 3) ^ a1 ^ a2 ^ multiply(a3, 2));
    }
}

static void inv_mix_columns(uint8_t state[4][4])
{
    for (int c = 0; c < 4; c++) {
        uint8_t a0 = state[0][c];
        uint8_t a1 = state[1][c];
        uint8_t a2 = state[2][c];
        uint8_t a3 = state[3][c];
        state[0][c] = (uint8_t)(multiply(a0, 0x0e) ^ multiply(a1, 0x0b) ^ multiply(a2, 0x0d) ^ multiply(a3, 0x09));
        state[1][c] = (uint8_t)(multiply(a0, 0x09) ^ multiply(a1, 0x0e) ^ multiply(a2, 0x0b) ^ multiply(a3, 0x0d));
        state[2][c] = (uint8_t)(multiply(a0, 0x0d) ^ multiply(a1, 0x09) ^ multiply(a2, 0x0e) ^ multiply(a3, 0x0b));
        state[3][c] = (uint8_t)(multiply(a0, 0x0b) ^ multiply(a1, 0x0d) ^ multiply(a2, 0x09) ^ multiply(a3, 0x0e));
    }
}

void aes128_encrypt_block(const aes128_ctx *ctx, const uint8_t in[16], uint8_t out[16])
{
    uint8_t state[4][4];

    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            state[r][c] = in[c * 4 + r];
        }
    }

    add_round_key(state, ctx->round_key);
    for (int round = 1; round < 10; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, ctx->round_key + round * 16);
    }
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, ctx->round_key + 160);

    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            out[c * 4 + r] = state[r][c];
        }
    }
}

void aes128_decrypt_block(const aes128_ctx *ctx, const uint8_t in[16], uint8_t out[16])
{
    uint8_t state[4][4];

    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            state[r][c] = in[c * 4 + r];
        }
    }

    add_round_key(state, ctx->round_key + 160);
    for (int round = 9; round > 0; round--) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, ctx->round_key + round * 16);
        inv_mix_columns(state);
    }
    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, ctx->round_key);

    for (int c = 0; c < 4; c++) {
        for (int r = 0; r < 4; r++) {
            out[c * 4 + r] = state[r][c];
        }
    }
}

void aes128_cbc_decrypt(const uint8_t key[16], uint8_t iv[16], const uint8_t *in, uint8_t *out, size_t len)
{
    aes128_ctx ctx;
    uint8_t block[16];
    uint8_t prev[16];
    uint8_t cipher[16];

    aes128_init(&ctx, key);
    memcpy(prev, iv, 16);

    for (size_t off = 0; off + 16 <= len; off += 16) {
        memcpy(cipher, in + off, 16);
        aes128_decrypt_block(&ctx, in + off, block);
        for (int i = 0; i < 16; i++) {
            out[off + i] = (uint8_t)(block[i] ^ prev[i]);
        }
        memcpy(prev, cipher, 16);
    }

    memcpy(iv, prev, 16);
}

static void inc_ctr(uint8_t ctr[16])
{
    for (int i = 15; i >= 0; i--) {
        ctr[i]++;
        if (ctr[i] != 0) {
            break;
        }
    }
}

void aes128_ctr_advance(uint8_t ctr[16], uint64_t blocks)
{
    for (int i = 15; i >= 0 && blocks; i--) {
        uint64_t sum = (uint64_t)ctr[i] + (blocks & 0xffu);
        ctr[i] = (uint8_t)sum;
        blocks = (blocks >> 8) + (sum >> 8);
    }
}

void aes128_ctr_xcrypt(const uint8_t key[16], uint8_t ctr[16], const uint8_t *in, uint8_t *out, size_t len)
{
    aes128_ctx ctx;
    uint8_t stream[16];
    size_t off = 0;

    aes128_init(&ctx, key);
    while (off < len) {
        size_t n = len - off;
        if (n > 16) {
            n = 16;
        }
        aes128_encrypt_block(&ctx, ctr, stream);
        for (size_t i = 0; i < n; i++) {
            out[off + i] = (uint8_t)(in[off + i] ^ stream[i]);
        }
        off += n;
        inc_ctr(ctr);
    }
}

static int same(const uint8_t *a, const uint8_t *b, size_t n)
{
    return memcmp(a, b, n) == 0;
}

